# arithmetic operators
plus = 1 + 2
print(plus)

multiplication = 1 * 2
print(multiplication)

division = 1 / 2
print(division)

# modulus operator
modulus = 2 % 3
print(modulus) # gives us the remainder

# exponential powers
exponent = 2 ** 3
print(exponent)

# flow division
flow_division = 10 // 3
print(flow_division)

# assignment operators
a = 5
b = 10
a +=3 # looks much cleaner than a = a + 3, this works with all of the operators
print(a)

# comparison operators
print( 10 == 5) # two equals it will return false
print(10 == 10) # it will return true

print(10 != 11) # checks whether is not equal to
print(9 < 10) # checks whether is smaller than
print(10 > 15) # checks whether is bigger than

print(a < b and 10 > 6) # it will return true
